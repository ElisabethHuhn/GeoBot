    GBCoordinateConstants(int zone) {
        if (zone == 0)return;
        int last = rawConstants.length;
        int position = 0;

        String constants;

        while (position < last){
            constants = rawConstants[position];
            getZoneConstants(constants);
            if (getZone() == zone)return;

            position++;
        }
        //if we fall out of the loop, the zone was not found
        setZone((int)GBUtilities.ID_DOES_NOT_EXIST);
    }

